    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        char genChar;
        
        if (rbFeme.isSelected()) {
            genChar = 'F';
        }else{
            genChar = 'M';
        }
        
        contactos.add(new Contacto(tfNombre.getText(), tfNumero.getText(), tfCorreo.getText(), tfDireccion.getText(),
        genChar, (Integer) spEdad.getValue()));
        
        jdNuevoContacto.dispose();
        
        tfNombre.setText("");
        tfNumero.setText("");
        tfCorreo.setText("");
        tfDireccion.setText("");
        spEdad.setValue((int) 0);
        
        rbMasc.setSelected(true);
        rbFeme.setSelected(false);
            
        refrescarContactos();
    }//GEN-LAST:event_jButton8ActionPerformed

