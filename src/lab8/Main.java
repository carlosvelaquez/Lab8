    private void llenarDatos() {
        if (cbModificarContacto.getItemCount() > 0) {
            Contacto c = (Contacto) cbModificarContacto.getSelectedItem();

            if (c.getGenero() == 'F') {
                rbFeme1.setSelected(true);
                rbMasc1.setSelected(false);
            }else{
                rbFeme1.setSelected(false);
                rbMasc1.setSelected(true);
            }

            tfNombre1.setText(c.getNombre());
            tfNumero1.setText(c.getNumero());
            tfCorreo1.setText(c.getCorreoElectronico());
            tfDireccion1.setText(c.getDireccion());

            spEdad.setValue((int) c.getEdad());
        }
        
    }

